    private void imagen1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_imagen1MouseClicked
        // TODO add your handling code here:
        MostrarLibro.setVisible(true);
        for(int i=0; i < catalogo.getTamanio(); i++){
            try {
                if(Libro1.getText().equals(catalogo.getValor(i).getTitulo())){
                    isbn.setText(Long.toString(catalogo.getValor(i).getISBN()));
                    titulo.setText(catalogo.getValor(i).getTitulo());
                    edicion.setText(catalogo.getValor(i).getEdicion());
                    idioma.setText(catalogo.getValor(i).getIdioma());
                    anio.setText(catalogo.getValor(i).getAnio());
                    editorial.setText(catalogo.getValor(i).getEditorial());
                    categoria.setText(catalogo.getValor(i).getCategoria());
                    resumen.setText(catalogo.getValor(i).getResumen());
                    DefaultTableModel temp = (DefaultTableModel) autores.getModel();
                    for(int j=0; j < catalogo.getValor(i).getListaAutores().getTamanio();j++){
                        Object nuevo[]= {catalogo.getValor(i).getListaAutores().getValor(j).getNombre(),catalogo.getValor(i).getListaAutores().getValor(j).getApellido()};
                        temp.addRow(nuevo);
                    }
                }
            } catch (PosicionIlegalException ex) {
                Logger.getLogger(GUITiendaLibros.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_imagen1MouseClicked

