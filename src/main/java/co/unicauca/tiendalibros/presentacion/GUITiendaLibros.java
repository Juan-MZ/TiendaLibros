    private void aceptareditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_aceptareditarActionPerformed
        Libro libro = new Libro();
        Lista<Autor> autoreseditar = new Lista<>();
        for(int i=0; i < catalogo.getTamanio();i++){
            try {
                if(catalogo.getValor(i).getTitulo().equals(titulo.getText())){
                    libro.setISBN(Long.parseLong(editarisbn.getText()));
                    libro.setTitulo(editartitulo.getText());
                    libro.setEdicion(editaredicion.getText());
                    libro.setIdioma(editaridioma.getSelectedItem().toString());
                    libro.setAnio(editaranio.getText());
                    libro.setEditorial(editareditorial.getText());
                    libro.setCategoria(editarcategoria.getSelectedItem().toString());
                    libro.setResumen(editarresumen.getText());
                    libro.setCaratuladellibro(catalogo.getValor(i).getCaratuladellibro());
                    for(int j=0;j<editarautores.getRowCount();j++){
                        Autor autor = new Autor();
                        autor.setNombre(editarautores.getValueAt(j, 0).toString());
                        autor.setApellido(editarautores.getValueAt(j, 1).toString());
                        autoreseditar.agregar(autor);
                    }
                    libro.setListaAutores(autoreseditar);
                    catalogo.remover(i);
                    catalogo.insertar(libro, catalogo.getTamanio());
                    Catalogo.guardarLibros(catalogo);
                    editarlibro.dispose();
                }
            } catch (PosicionIlegalException ex) {
                Logger.getLogger(GUITiendaLibros.class.getName()).log(Level.SEVERE, null, ex);
            } catch (IOException ex) {
                Logger.getLogger(GUITiendaLibros.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_aceptareditarActionPerformed

